/**
 * İlçe adını slug'a çevirir
 */
export function districtToSlug(name: string): string {
  return provinceToSlug(name); // Aynı mantık
}

/**
 * Bir ilin ilçelerini getirir
 */
export function getDistrictsByCity(citySlug: string): string[] {
  return DISTRICTS[citySlug.toLowerCase()] || [];
}

/**
 * İl ve ilçe kombinasyonunun geçerli olup olmadığını kontrol eder
 */
export function isValidCityDistrict(
  citySlug: string,
  districtSlug: string
): boolean {
  const districts = getDistrictsByCity(citySlug);
  return districts.some((d) => districtToSlug(d) === districtSlug.toLowerCase());
}
