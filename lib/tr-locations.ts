/**
 * İl adını slug'a çevirir
 * Örnek: "İstanbul" → "istanbul"
 */
export function provinceToSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/ğ/g, 'g')
    .replace(/ü/g, 'u')
    .replace(/ş/g, 's')
    .replace(/ı/g, 'i')
    .replace(/ö/g, 'o')
    .replace(/ç/g, 'c')
    .replace(/\s+/g, '-');
}

/**
 * Slug'ı il adına çevirir
 * Örnek: "istanbul" → "İstanbul"
 */
export function slugToProvince(slug: string): string | null {
  const found = PROVINCES.find(
    (province) => provinceToSlug(province) === slug.toLowerCase()
  );
  return found || null;
}

